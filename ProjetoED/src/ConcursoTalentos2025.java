    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        if (jComboBox2.getSelectedItem() == null) {
            jTextArea1.setText("Nenhum gênero selecionado!");
            return;
        }

        String generoSelecionado = jComboBox2.getSelectedItem().toString().toLowerCase();
        String texto = "Candidato do gênero \"" + generoSelecionado + "\":\n\n";
        boolean encontrou = false;

        for (Candidato c : todosCandidatos) {
            if (c.getGeneroMusical().toLowerCase().equals(generoSelecionado)) {
                texto += c.toString() + "\n\n";
                encontrou = true;
            }
        }
        if (!encontrou) {
            texto += "Nenhum candidato encontrado!";
        }

        jTextArea1.setText(texto);
    }//GEN-LAST:event_jButton10ActionPerformed

