    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String nome = txtNome.getText().trim();
            int idade = Integer.parseInt(txtIdade.getText().trim());
            String sexoStr = jComboBox1.getSelectedItem().toString();
            char sexo = sexoStr.equalsIgnoreCase("Feminino") ? 'F' : 'M';
            String pais = txtPais.getText().trim();
            String generoMusical = txtGenero.getText().trim();

            if (nome.isEmpty() || pais.isEmpty() || generoMusical.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha todos os campos!");
                return;
            }

            if (idade <= 0) {
                JOptionPane.showMessageDialog(this, "Idade inválida!");
                return;
            }
            Candidato c = new Candidato(nome, idade, sexo, pais, generoMusical);
            todosCandidatos.add(c);

            String novoGenero = generoMusical.toLowerCase();

            boolean jaExiste = false;
            for (int i = 0; i < jComboBox2.getItemCount(); i++) {
                if (jComboBox2.getItemAt(i).toLowerCase().equals(novoGenero)) {
                    jaExiste = true;
                    break;
                }
            }
            if (!jaExiste) {
                jComboBox2.addItem(generoMusical);
            }

            if (sexo == 'F') {
                pilhaMulher.push(c);
            } else {
                filaHomens.add(c);
            }
            JOptionPane.showMessageDialog(this, "Candidato cadastrado com sucesso!");
            txtNome.setText("");
            txtIdade.setText("");
            jComboBox1.setSelectedIndex(0);
            txtPais.setText("");
            txtGenero.setText("");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Idade deve ser um número válido!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar " + e.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        ArrayList<Candidato> mulheres = new ArrayList<>(pilhaMulher);
        ArrayList<Candidato> homens = new ArrayList<>(filaHomens);

        mulheres.sort((c1, c2) -> c1.getNome().compareToIgnoreCase(c2.getNome()));
        homens.sort((c1, c2) -> c1.getNome().compareToIgnoreCase(c2.getNome()));

        StringBuilder resultado = new StringBuilder();

        resultado.append("=== Mulheres ===\n");
        for (Candidato c : mulheres) {
            resultado.append(c.toString()).append("\n\n");
        }
        resultado.append("=== Homens ===\n");
        for (Candidato c : homens) {
            resultado.append(c.toString()).append("\n\n");
        }

        jTextArea1.setText(resultado.toString());
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (todosCandidatos.isEmpty()) {
            jTextArea1.setText("Nenhum candidato cadastrado!");
            return;
        }

        int soma = 0;
        for (Candidato c : todosCandidatos) {
            soma += c.getIdade();
        }
        double media = (double) soma / todosCandidatos.size();
        String mediaFormatada = String.format(Locale.US, "%.2f", media);
        jTextArea1.setText("Média geral das idades: " + mediaFormatada);
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (pilhaMulher.isEmpty()) {
            jTextArea1.setText("Nenhuma candidata cadastrada!");
            return;
        }

        Candidato maisJovem = pilhaMulher.get(0);

        for (int i = 1; i < pilhaMulher.size(); i++) {
            if (pilhaMulher.get(i).getIdade() < maisJovem.getIdade()) {
                maisJovem = pilhaMulher.get(i);
            }
        }
        jTextArea1.setText("Candidata mais jovem:\n\n" + maisJovem);
    }//GEN-LAST:event_jButton4ActionPerformed

